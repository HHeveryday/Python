'''判断字符串是否合法（由字母、数字、下划线）'''
print('hello'.isidentifier())
'''判断字符串是否全为空白字符(回车、换行、水平制表符)组成'''
print('\t'.isspace())
'''判断字符串是否全部为字母组成'''

print('hello1'.isalpha())
'''判断字符串是否全部由十进制数组成'''
print('123'.isdecimal())
'''判断指定的字符串是否全部由数字组成'''

print('123'.isnumeric())
'''判断指定字符是否全部为字母和数字组成'''
print('sjd12'.isalnum())
